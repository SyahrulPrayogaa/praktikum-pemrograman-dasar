# =========Set==================

# Membuat Set
car = {"BMW", "Tesla", "Mercedes"}

# mengakses Set
print(car)

# menambahkan Set
car.add('Toyota')
print('=========Menambahkan Set=========')
print(car)

# Mengubah Set
car.update({'BMW', 'Honda'})
print('=========Mengubah Set=========')
print(car)

# menghapus item Set
print('=========Menghapus Set=========')
car.clear()
print(car)
